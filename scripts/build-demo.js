/**
 * Normalize example lesson data to engine format
 * - goals → learningGoals
 * - flat dialog[] → structured { title, lines: [{ person, text }] }
 */
function normalizeLessonData(lesson) {
    const normalized = {};

    // goals → learningGoals
    normalized.learningGoals = lesson.learningGoals || lesson.goals || [];

    // Normalize dialog format
    if (Array.isArray(lesson.dialog)) {
        // Flat array format → structured
        const firstLine = lesson.dialog[0];
        normalized.dialog = {
            title: lesson.title || '',
            description: lesson.subtitle || '',
            lines: lesson.dialog.map(line => ({
                person: line.speaker || line.person,
                text: line.text,
                translation: line.translation
            }))
        };
    } else if (lesson.dialog) {
        // Already structured
        normalized.dialog = lesson.dialog;
    }

    // Pass through other fields
    normalized.checklist = lesson.checklist || [];
    normalized.vocabulary = lesson.vocabulary;
    normalized.additionalTexts = lesson.additionalTexts;
    normalized.phraseTables = lesson.phraseTables;
    normalized.factCards = lesson.factCards;

    // Store original title for template generation
    normalized._originalTitle = lesson.title;

    return normalized;
}

/**
 * Check if a chapter has extra exercises
 */
async function checkHasExtraExercises(chapter, curriculum) {
    try {
        const exerciseFile = path.join(contentDir, 'exercises-data', curriculum.id, `chapter-${chapter}.js`);
        if (!fs.existsSync(exerciseFile)) return false;
        const mod = await import(`file://${exerciseFile}`);
        for (const data of Object.values(mod.exercisesData)) {
            if (data.extraExercises && data.extraExercises.length > 0) return true;
        }
    } catch { /* ignore */ }
    return false;
}

/**
 * Generate the extra exercises tab HTML
 */
function generateExtraExercisesTab() {
    return `
            <div class="tab-content hidden space-y-8" id="extra-exercises">
                <div id="extra-exercises-content">
                    <!-- Extra exercises loaded dynamically -->
                </div>
                <div class="mt-12 p-6 bg-neutral-100/60 rounded-lg text-center">
                    <button
                        class="next-tab-btn bg-primary-500 text-white font-bold py-3 px-5 rounded-lg hover:bg-primary-600 transition-colors"
                        data-next-tab="dialog" data-i18n="btn_next">
                        Next
                    </button>
                </div>
            </div>`;
}

/**
 * Generate the demo homepage
 */
function generateHomepage(config, curriculum, lessons, chapterTitles) {
    const lessonEntries = Object.entries(lessons);

    // Group by chapter
    const chapters = {};
    for (const [lessonId, data] of lessonEntries) {
        const [ch] = lessonId.split('-');
        if (!chapters[ch]) chapters[ch] = [];
        const fileName = `${curriculum.filePrefix}-${lessonId.replace('-', '-')}.html`;
        chapters[ch].push({
            id: lessonId,
            title: data._originalTitle || data.dialog?.title || `Lesson ${lessonId}`,
            fileName,
            goals: data.learningGoals || []
        });
    }

    const chaptersHtml = Object.entries(chapters).map(([ch, chapterLessons]) => {
        const lessonsHtml = chapterLessons.map(l => `
                    <a href="lessons/${l.fileName}" class="block p-4 bg-surface rounded-lg shadow-sm hover:shadow-md transition-shadow border border-neutral-200 hover:border-primary-300">
                        <h3 class="text-lg font-bold text-primary-700">${escapeHtml(l.title)}</h3>
                        <p class="text-sm text-neutral-500 mt-1">Lesson ${l.id}</p>
                        ${l.goals.length > 0 ? `<ul class="mt-2 text-sm text-neutral-600 list-disc list-inside">${l.goals.slice(0, 2).map(g => `<li>${escapeHtml(g)}</li>`).join('')}</ul>` : ''}
                    </a>`).join('\n');

        return `
                <div class="mb-10">
                    <h2 class="text-2xl font-bold text-primary-800 mb-4">Chapter ${ch}: ${chapterTitles[ch] || ''}</h2>
                    <div class="grid sm:grid-cols-2 gap-4">
                        ${lessonsHtml}
                    </div>
                </div>`;
    }).join('\n');

    const html = `<!DOCTYPE html>
<html lang="en">
<head>
    <meta charset="utf-8" />
    <meta content="width=device-width, initial-scale=1.0" name="viewport" />
    <title>${escapeHtml(config.name)} — Demo</title>
    <link href="papertek.css" rel="stylesheet" />
    <link href="stylesheet.css" rel="stylesheet" />
    <link href="manifest.json" rel="manifest" />
    <meta content="yes" name="apple-mobile-web-app-capable" />
</head>
<body class="bg-neutral-50 text-neutral-800">
    <div class="container mx-auto p-4 sm:p-6 md:p-8 max-w-4xl">
        <header class="text-center mb-12">
            <div class="inline-block bg-primary-100 text-primary-700 text-sm font-medium px-3 py-1 rounded-full mb-4">
                Papertek Demo
            </div>
            <h1 class="text-4xl md:text-5xl font-bold text-primary-700 tracking-tight">
                ${escapeHtml(config.name)}
            </h1>
            <p class="mt-3 text-lg text-neutral-600 max-w-2xl mx-auto">
                ${escapeHtml(config.description)}
            </p>
            <div class="mt-4 flex justify-center gap-4 text-sm text-neutral-500">
                <span>${curriculum.chapters} chapters</span>
                <span>·</span>
                <span>${Object.keys(lessons).length} lessons</span>
                <span>·</span>
                ${curriculum.cefrLevel ? `<span>CEFR ${curriculum.cefrLevel}</span>` : ''}
            </div>
        </header>

        <main>
            ${chaptersHtml}
        </main>

        <footer class="mt-16 pt-8 border-t border-neutral-200 text-center text-sm text-neutral-500">
            <p>Built with <a href="https://github.com/Papertek-forlag-AS/edu-framework" class="text-primary-600 hover:underline">Papertek — Framework for Education</a></p>
        </footer>
    </div>
</body>
</html>`;

    fs.writeFileSync(path.join(distDir, 'index.html'), html);
}

/**
 * Recursively copy a directory
 */
function copyRecursive(src, dest) {
    if (!fs.existsSync(src)) return;

    if (!fs.existsSync(dest)) {
        fs.mkdirSync(dest, { recursive: true });
    }

    const entries = fs.readdirSync(src, { withFileTypes: true });
    for (const entry of entries) {
        const srcPath = path.join(src, entry.name);
        const destPath = path.join(dest, entry.name);

        if (entry.isDirectory()) {
            copyRecursive(srcPath, destPath);
        } else {
            fs.copyFileSync(srcPath, destPath);
        }
    }
}

/**
 * Escape HTML special characters
 */
function escapeHtml(str) {
    if (!str) return '';
    return str.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');
}
