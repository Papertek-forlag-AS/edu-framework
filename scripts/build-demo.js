#!/usr/bin/env node

/**
 * build-demo.js — Assembles a standalone demo from an example course + engine
 *
 * Usage: node scripts/build-demo.js [example-name]
 * Default example: german-a1
 *
 * Output: dist/ directory ready to serve
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');

// ─── Configuration ───────────────────────────────────────────────────
const exampleName = process.argv[2] || 'german-a1';
const exampleDir = path.join(ROOT, 'examples', exampleName);
const distDir = path.join(ROOT, 'dist');
const engineDir = path.join(ROOT, 'public');
const templatePath = path.join(ROOT, 'public/js/lesson-template/template-international.html');

if (!fs.existsSync(exampleDir)) {
    console.error(`❌ Example "${exampleName}" not found at ${exampleDir}`);
    process.exit(1);
}

// ─── Load example config ─────────────────────────────────────────────
const configPath = path.join(exampleDir, 'papertek.config.js');
const config = (await import(`file://${configPath}`)).default;
const curriculum = config.curricula[0];
const contentLanguage = curriculum.contentPath.split('/').pop(); // "german"
const contentDir = path.join(exampleDir, curriculum.contentPath);

console.log(`🔧 Building demo: ${config.name}`);
console.log(`   Curriculum: ${curriculum.id}`);
console.log(`   Chapters: ${curriculum.chapters}, Lessons/chapter: ${curriculum.lessonsPerChapter}`);

// ─── Clean and create dist ───────────────────────────────────────────
if (fs.existsSync(distDir)) {
    fs.rmSync(distDir, { recursive: true });
}

const dirs = [
    distDir,
    path.join(distDir, 'js'),
    path.join(distDir, 'lessons'),
    path.join(distDir, 'content', contentLanguage),
    path.join(distDir, 'content', contentLanguage, 'exercises-data', curriculum.id),
    path.join(distDir, 'ikoner'),
];
dirs.forEach(d => fs.mkdirSync(d, { recursive: true }));

// ─── 1. Copy engine (JS) ────────────────────────────────────────────
console.log('📦 Copying engine files...');
copyRecursive(path.join(engineDir, 'js'), path.join(distDir, 'js'));

// ─── 2. Copy CSS ────────────────────────────────────────────────────
for (const cssFile of ['papertek.css', 'stylesheet.css']) {
    const src = path.join(engineDir, cssFile);
    if (fs.existsSync(src)) {
        fs.copyFileSync(src, path.join(distDir, cssFile));
    }
}

// ─── 3. Build monolithic lesson data ─────────────────────────────────
console.log('📝 Building lesson content...');
const allLessons = {};
const chapterTitles = {};

for (let ch = 1; ch <= curriculum.chapters; ch++) {
    const chapterFile = path.join(contentDir, 'lessons-data', `chapter-${ch}.js`);
    if (!fs.existsSync(chapterFile)) {
        console.warn(`   ⚠️ Missing ${chapterFile}`);
        continue;
    }

    const mod = await import(`file://${chapterFile}`);
    const raw = mod.lessonsData;

    // Build chapter title from first lesson
    const firstLessonId = `${ch}-1`;
    if (raw[firstLessonId]) {
        chapterTitles[ch] = `Chapter ${ch}`;
    }

    // Normalize each lesson
    for (const [lessonId, lesson] of Object.entries(raw)) {
        allLessons[lessonId] = normalizeLessonData(lesson);
    }
}

const lessonsFileContent = `// Auto-generated by build-demo.js
export const chapterTitles = ${JSON.stringify(chapterTitles, null, 2)};

export const lessonsData = ${JSON.stringify(allLessons, null, 2)};
`;

const contentSuffix = curriculum.id;
fs.writeFileSync(
    path.join(distDir, 'content', contentLanguage, `lessons-data-${contentSuffix}.js`),
    lessonsFileContent
);

// ─── 4. Build monolithic grammar data ────────────────────────────────
console.log('📝 Building grammar content...');
const allGrammar = {};

for (let ch = 1; ch <= curriculum.chapters; ch++) {
    const grammarFile = path.join(contentDir, 'grammar-data', `chapter-${ch}.js`);
    if (!fs.existsSync(grammarFile)) continue;

    const mod = await import(`file://${grammarFile}`);
    const raw = mod.grammarModules;

    // Expand chapter-keyed to lesson-keyed
    for (const [chapterKey, modules] of Object.entries(raw)) {
        for (let lesson = 1; lesson <= curriculum.lessonsPerChapter; lesson++) {
            const lessonId = `${chapterKey}-${lesson}`;
            // Only assign if lesson exists in lesson data
            if (allLessons[lessonId]) {
                allGrammar[lessonId] = modules;
            }
        }
    }
}

const grammarFileContent = `// Auto-generated by build-demo.js
export const grammarData = ${JSON.stringify(allGrammar, null, 2)};
`;

fs.writeFileSync(
    path.join(distDir, 'content', contentLanguage, `grammar-data-${contentSuffix}.js`),
    grammarFileContent
);

// ─── 5. Copy exercise files as-is (already per-chapter) ─────────────
console.log('📝 Copying exercise files...');
const exerciseSrcDir = path.join(contentDir, 'exercises-data', curriculum.id);
const exerciseDestDir = path.join(distDir, 'content', contentLanguage, 'exercises-data', curriculum.id);

if (fs.existsSync(exerciseSrcDir)) {
    copyRecursive(exerciseSrcDir, exerciseDestDir);
}

// ─── 6. Create stub files for missing content ────────────────────────
console.log('📝 Creating stub content files...');

const stubs = {
    [`pronunciation-data-${contentSuffix}.js`]: 'export const pronunciationData = {};',
    [`culture-data-${contentSuffix}.js`]: 'export const cultureData = {};',
    'vocabulary-data.js': 'export const vocabularyData = {};',
    'characters-data.js': `export function getCharacterName(name) { return name; }`,
};

for (const [filename, content] of Object.entries(stubs)) {
    fs.writeFileSync(
        path.join(distDir, 'content', contentLanguage, filename),
        `// Auto-generated stub by build-demo.js\n${content}\n`
    );
}

// ─── 7. Generate lesson HTML files ───────────────────────────────────
console.log('📄 Generating lesson pages...');
const template = fs.readFileSync(templatePath, 'utf-8');

// Fix template paths for demo structure: lessons are in lessons/ subfolder
// Template expects ../js/main.js (from lessons/ → dist root → js/)
// Template expects ../papertek.css (from lessons/ → dist root)
// These are already correct for our structure

for (const [lessonId, lessonData] of Object.entries(allLessons)) {
    const [chapter, lesson] = lessonId.split('-');
    const fileName = `${curriculum.filePrefix}-${chapter}-${lesson}.html`;
    const title = lessonData.dialog?.title || lessonData._originalTitle || `Lesson ${chapter}.${lesson}`;
    const lessonNumber = `${chapter}.${lesson}`;

    // Check if this lesson has extra exercises
    const hasExtras = await checkHasExtraExercises(chapter, curriculum);

    let html = template
        .replace(/\{\{LESSON_NUMBER\}\}/g, lessonNumber)
        .replace(/\{\{TITLE\}\}/g, escapeHtml(title))
        .replace(/\{\{CHAPTER_ID\}\}/g, lessonId)
        .replace(/\{\{FILE_NAME\}\}/g, fileName)
        .replace(/\{\{LESSON_DOT_NUMBER\}\}/g, lessonNumber);

    // Handle extra exercises tab
    if (hasExtras) {
        html = html.replace('{{EKSTRAOVELSER_TAB}}', generateExtraExercisesTab());
    } else {
        html = html.replace('{{EKSTRAOVELSER_TAB}}', '');
    }

    // Fix content path for demo structure
    // The content-loader resolves paths relative to the JS file location
    // We need the international page detection to work, which it will via data-ui-language="en"

    // Fix start button text
    html = html.replace('{{START_BUTTON_TEXT}}', 'Start');

    // Fix service worker path
    html = html.replace("'./sw-international.js'", "'../sw-international.js'");
    html = html.replace("{ scope: '/international/' }", "{ scope: '/' }");

    // Fix manifest path
    html = html.replace('href="./manifest.json"', 'href="../manifest.json"');

    fs.writeFileSync(path.join(distDir, 'lessons', fileName), html);
    console.log(`   ✅ ${fileName}`);
}

// ─── 8. Generate homepage ────────────────────────────────────────────
console.log('🏠 Generating homepage...');
generateHomepage(config, curriculum, allLessons, chapterTitles);

// ─── 9. Generate manifest.json ───────────────────────────────────────
const manifest = {
    name: config.name,
    short_name: curriculum.id,
    start_url: '.',
    display: 'standalone',
    background_color: '#f8fafc',
    theme_color: '#2563eb',
    icons: []
};
fs.writeFileSync(path.join(distDir, 'manifest.json'), JSON.stringify(manifest, null, 2));

// ─── 10. Generate minimal service worker ─────────────────────────────
fs.writeFileSync(path.join(distDir, 'sw-international.js'), `// Minimal service worker for demo
self.addEventListener('install', () => self.skipWaiting());
self.addEventListener('activate', (event) => event.waitUntil(self.clients.claim()));
`);

console.log('\n✅ Demo built successfully!');
console.log(`   Output: ${distDir}/`);
console.log(`   Lessons: ${Object.keys(allLessons).length}`);
console.log(`\n   Run: npx http-server dist -p 8080`);

